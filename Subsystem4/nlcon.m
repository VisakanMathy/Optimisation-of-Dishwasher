function [c,ceq] = nlcon(x)
    Lb = 0.5;
    g=-9.81;
    vp = 1.953;
    ceq(1) = ((Lb-x(3))/2)-0.05 - (x(4)*(x(2)-1)+x(5));
    c(1) = -0.3+(((-(x(6)*cosd(x(1))))^2)/2*g);
    ceq(2) = -0.03+((((vp)^2-(x(6)*sind(x(1))))^2));
    c(2) = +x(6)*pi*x(3)^2 -0.1;
    c(3) = -x(6)*pi*x(3)^2 +0.05;
    ceq(3) = checkForInt(x(2));
    %c=0;